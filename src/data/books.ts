export const getFeaturedBooks = () => books.filter((b) => b.featured);
export const getBookById = (id: string) => books.find((b) => b.id === id);
export const getBooksByCategory = (cat: string) =>
  books.filter((b) => b.category === cat);
export const searchBooks = (query: string) => {
  const q = query.toLowerCase();
  return books.filter(
    (b) =>
      b.title.toLowerCase().includes(q) ||
      b.courseCode.toLowerCase().includes(q) ||
      b.author.toLowerCase().includes(q) ||
      b.department.toLowerCase().includes(q),
  );
};
